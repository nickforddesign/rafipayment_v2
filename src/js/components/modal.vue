<template>
  <transition name="fade">
    <div class="modal-container" @click.self="closeModal">
      <div class="modal" @keyup.esc="handleEscape" @keyup.enter="handleEnter">
        <loading v-if="loading"></loading>
        <div class="modal-header">
          <button type="button" class="link close" @click="cancel">
            {{ cancel_label }}
          </button>
          <button type="button" class="link confirm" @click="validate" v-if="has_confirm">
            {{ confirm_label }}
          </button>

          <slot name="header">
            <h1>Please confirm</h1>
          </slot>
        </div>
        <div class="modal-body">
          <slot name="body">test</slot>
        </div>
      </div>
    </div>
  </transition>
</template>

<script>
import { path } from 'ramda'

export default {
  name: 'modal',
  props: {
    confirm: Function,
    keywatch: {
      type: Boolean,
      default: true
    }
  },
  data() {
    return {
      loading: false
    }
  },
  mounted() {
    try {
      const default_focus = path(['$refs', 'default'], this.$parent)
      if (default_focus) {
        default_focus.focus()
      }
    } catch (error) {
      console.warn(error)
    }
    document.body.classList.add('lock')
  },
  beforeDestroy() {
    document.body.classList.remove('lock')
  },
  computed: {
    has_confirm() {
      return this.confirm !== undefined
    },
    cancel_label() {
      return 'Cancel'
      // return this.has_confirm
      //   ? 'Cancel'
      //   : 'OK'
    },
    confirm_label() {
      return 'OK'
    }
  },
  methods: {
    handleEnter() {
      if (this.keywatch) {
        this.validate()
      }
    },
    handleEscape() {
      if (this.keywatch) {
        this.close()
      }
    },
    async validate(e) {
      this.loading = true
      try {
        if (this.confirm) {
          await this.confirm()
        }
        this.close()
      } catch (err) {
        console.warn(err)
      } finally {
        this.$emit('complete')
        this.loading = false
      }
    },
    cancel() {
      this.close()
    },
    close() {
      this.$emit('close')
    }
  }
}
</script>

